// calculator

#include <iostream>
using namespace std;

int main()
{
    int a = 3, b = 19;

    cout << " sum is : " << a + b << endl;
    cout << " diff is : " << a - b << endl;
    cout << " product is : " << a * b << endl;
    cout << " quotient is : " << a / b << endl;
    cout << " remainder is : " << a % b << endl;

    return 0;
}